package Operators;
import java.util.*;
public class RelationalOperators {
	public static void main(String args[])
	{
		Scanner sc = new Scanner(System.in);
		int a=sc.nextInt();
		int b=sc.nextInt();
		
		// == operator
		
		System.out.println(a==b);
		
		// != 
		
		System.out.println(a!=b);
		
		// >
		
		System.out.println(a>b);
		
		// < 
		
		System.out.println(a<b);
		
		// >= 
		
		System.out.println(a>=b);
		
		//<=
		
		System.out.println(a<=b);
		
	}
}
