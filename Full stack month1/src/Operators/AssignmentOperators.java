package Operators;

public class AssignmentOperators {
	public static void main(String args[])
	{
		// = operator
		
		int a = 3;
		 
		// += operator 
		
		int b=1;
		b+=a; // same as b=b+a;
		
		int c=10;
		
		c-=a;
		
		// *= 
		
		int d=1;
		d*=c;
		// /= 
		d/=c;
		
		// %=
		int e=31;
		e%=a;
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		
		
	}
}
